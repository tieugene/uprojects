package szeng::Object;

require 5.005;

use strict;
#use utf8;
use Tie::Hash;

use vars qw($VERSION @ISA);
@ISA     	= qw(Tie::StdHash);

$VERSION = "0.0.1";


# ------------------------------------------------------------------------------------------------------------------------------
sub Version { 
    $VERSION; 
}
# ------------------------------------------------------------------------------------------------------------------------------
sub outer {
  my $self = shift;
  return $self if tied(%$self);
  my %outer;
  tie %outer, ref($self), $self;
  ++$self->{net_ldap_refcnt};
  bless \%outer, ref($self);
}
# ------------------------------------------------------------------------------------------------------------------------------
sub inner {
  tied(%{$_[0]}) || $_[0];
}
# ------------------------------------------------------------------------------------------------------------------------------
sub TIEHASH {
  $_[1];
}
# ------------------------------------------------------------------------------------------------------------------------------
1;
